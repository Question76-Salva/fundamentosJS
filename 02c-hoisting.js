/*
    Al ejecutar un fragmento de código, el motor de JS
    crea el contexto de ejecución global.

    El contexto tiene 2 fases: Creación y Ejecución.

    Durante la fase de creación, el motor de js mueve 
    las declaraciones de variable y funciones a la parte
    superior de su contexto (scope).

    Eso nos permite invocar una función antes de definir
    esta función.

    Ej JS el hoisting funciona sólamente con las declaraciones.

*/
