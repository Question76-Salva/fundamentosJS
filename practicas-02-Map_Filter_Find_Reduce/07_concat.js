// ==============
// === concat ===
// ==============

// Se usa para "unir" dos o más arrays. Este método no cambia/muta los array existentes, sino que devuelve un nuevo array.

const array1 = ["a", "b", "c"];
const array2 = ["d", "e", "f"];
const array3 = array1.concat(array2);

console.log(array3);